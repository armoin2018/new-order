// ─────────────────────────────────────────────────────────────────────────────
// Engine class
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Pure-function engine for computing personality drift, trauma response, and
 * stress inoculation.
 *
 * All methods are deterministic and side-effect-free.  They consume immutable
 * inputs and produce new result objects.  Random elements (e.g. nuclear strike
 * escalate-vs-retreat) are intentionally *not* resolved here — callers must
 * roll their own RNG and use the probability fields on the result.
 *
 * @see FR-1505 — Personality drift from accumulated experience
 * @see FR-1506 — Trauma response from catastrophic events
 * @see FR-1507 — Stress inoculation after prolonged high-stress exposure
 */
export class PersonalityDriftEngine {
  private readonly cfg: PersonalityDriftConfig;

  constructor(config: PersonalityDriftConfig = GAME_CONFIG.psychology) {
    this.cfg = config;
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Stress Drift
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute personality drift from prolonged high stress.
   *
   * If the leader has been above the stress threshold for enough consecutive
   * turns, their risk tolerance shifts: escalators and lash-out types become
   * *more* risk-tolerant, while consolidators and retreaters become *less*
   * risk-tolerant.
   *
   * @param input — Current stress-drift context.
   * @returns The drift result including any produced {@link DriftEvent}.
   * @see FR-1505
   */
  computeStressDrift(input: StressDriftInput): StressDriftResult {
    const pd = this.cfg.personalityDrift;

    const meetsConsecutiveTurns =
      input.consecutiveHighStressTurns >= pd.stressDriftTurns;
    const meetsStressThreshold =
      input.currentStress >= pd.stressDriftThreshold;

    if (!meetsConsecutiveTurns || !meetsStressThreshold) {
      return {
        drifted: false,
        newRiskTolerance: input.currentRiskTolerance,
        delta: 0,
        driftEvent: null,
        reason: !meetsConsecutiveTurns
          ? `Leader has ${input.consecutiveHighStressTurns}/${pd.stressDriftTurns} consecutive high-stress turns — drift not triggered`
          : `Current stress ${input.currentStress} below threshold ${pd.stressDriftThreshold} — drift not triggered`,
      };
    }

    const isAggressive =
      input.stressResponse === 'escalate' ||
      input.stressResponse === 'lash_out';
    const delta = isAggressive
      ? pd.riskToleranceShift
      : -pd.riskToleranceShift;
    const newRiskTolerance = PersonalityDriftEngine.clamp(
      input.currentRiskTolerance + delta,
      0,
      100,
    );

    const driftEvent: DriftEvent = {
      trigger: `stress_drift_${input.stressResponse}`,
      eventId: null,
      turn: input.currentTurn,
      dimension: 'riskTolerance' as keyof LeaderPsychology,
      delta,
    };

    return {
      drifted: true,
      newRiskTolerance,
      delta,
      driftEvent,
      reason: `${input.consecutiveHighStressTurns} consecutive high-stress turns with stress ${input.currentStress} — ` +
        `${isAggressive ? 'aggressive' : 'defensive'} response shifts risk tolerance by ${delta > 0 ? '+' : ''}${delta} to ${newRiskTolerance}`,
    };
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Trauma Response
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute the psychological impact of a catastrophic event.
   *
   * Uses an exhaustive switch on {@link TraumaType} to ensure every trauma
   * variant is handled.  Each variant produces different emotional effects,
   * power-base adjustments, and potential stress-response overrides.
   *
   * @param input — The trauma context.
   * @returns A full {@link TraumaResult} describing all psychological impacts.
   * @see FR-1506
   */
  computeTraumaResponse(input: TraumaInput): TraumaResult {
    const tr = this.cfg.traumaResponse;

    switch (input.traumaType) {
      case TraumaType.NuclearStrike: {
        const cfg = tr.nuclearStrike;
        const driftEvents: DriftEvent[] = [
          {
            trigger: 'trauma_nuclear_strike_fear',
            eventId: null,
            turn: input.currentTurn,
            dimension: 'riskTolerance' as keyof LeaderPsychology,
            delta: -cfg.fearPermanentMin,
          },
        ];
        return {
          traumaType: input.traumaType,
          emotionalEffects: {
            fear: cfg.fearPermanentMin,
          },
          powerBaseEffects: {},
          fatigueMultiplier: 1,
          fatigueMultiplierDuration: 0,
          stressResponseOverride:
            cfg.escalateChance >= cfg.retreatChance ? 'escalate' : 'retreat',
          driftEvents,
          reason: `Nuclear strike trauma — fear floor set to ${cfg.fearPermanentMin}, ` +
            `${cfg.escalateChance * 100}% chance of escalation vs ${cfg.retreatChance * 100}% retreat`,
        };
      }

      case TraumaType.AssassinationAttempt: {
        const cfg = tr.assassinationAttempt;
        const driftEvents: DriftEvent[] = [
          {
            trigger: 'trauma_assassination_paranoia',
            eventId: null,
            turn: input.currentTurn,
            dimension: 'paranoia' as keyof LeaderPsychology,
            delta: cfg.paranoiaBoost,
          },
        ];
        return {
          traumaType: input.traumaType,
          emotionalEffects: {
            paranoia: cfg.paranoiaBoost,
          },
          powerBaseEffects: {
            securityServices: cfg.securityPowerBaseBoost,
            public: cfg.publicPowerBasePenalty,
          },
          fatigueMultiplier: 1,
          fatigueMultiplierDuration: 0,
          stressResponseOverride: null,
          driftEvents,
          reason: `Assassination attempt — paranoia +${cfg.paranoiaBoost}, ` +
            `security services power +${cfg.securityPowerBaseBoost}, ` +
            `public support ${cfg.publicPowerBasePenalty}`,
        };
      }

      case TraumaType.CapitalSiege: {
        const cfg = tr.capitalSiege;
        const driftEvents: DriftEvent[] = [
          {
            trigger: 'trauma_capital_siege_stress',
            eventId: null,
            turn: input.currentTurn,
            dimension: 'patience' as keyof LeaderPsychology,
            delta: -cfg.emotionalSpikeTo,
          },
        ];
        return {
          traumaType: input.traumaType,
          emotionalEffects: {
            stress: cfg.emotionalSpikeTo,
            fear: cfg.emotionalSpikeTo,
            anger: cfg.emotionalSpikeTo,
            confidence: -cfg.emotionalSpikeTo,
            resolve: cfg.emotionalSpikeTo,
          },
          powerBaseEffects: {},
          fatigueMultiplier: cfg.fatigueMultiplier,
          fatigueMultiplierDuration: cfg.fatigueMultiplierDuration,
          stressResponseOverride: null,
          driftEvents,
          reason: `Capital siege — all emotions spike to ${cfg.emotionalSpikeTo}, ` +
            `fatigue ${cfg.fatigueMultiplier}x for ${cfg.fatigueMultiplierDuration} turns`,
        };
      }

      default: {
        // Exhaustive check — TypeScript will error if a TraumaType variant is unhandled.
        const _exhaustive: never = input.traumaType;
        throw new Error(`Unhandled trauma type: ${_exhaustive as string}`);
      }
    }
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Betrayal Drift
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute paranoia drift after an ally's betrayal.
   *
   * Betrayal always increases paranoia by the configured amount, clamped to
   * the 0–100 range.
   *
   * @param input — Betrayal context.
   * @returns The drift result.
   * @see FR-1505
   */
  computeBetrayalDrift(input: BetrayalDriftInput): BetrayalDriftResult {
    const delta = this.cfg.personalityDrift.betrayalParanoiaIncrease;
    const newParanoia = PersonalityDriftEngine.clamp(
      input.currentParanoia + delta,
      0,
      100,
    );

    const driftEvent: DriftEvent = {
      trigger: 'betrayal_paranoia_drift',
      eventId: null,
      turn: input.currentTurn,
      dimension: 'paranoia' as keyof LeaderPsychology,
      delta,
    };

    return {
      newParanoia,
      delta,
      driftEvent,
      reason: `Ally betrayal — paranoia +${delta} (${input.currentParanoia} → ${newParanoia})`,
    };
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Victory Drift
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute psychological drift from a major victory.
   *
   * Victories boost both confidence and narcissism, reflecting the leader's
   * growing self-assurance and sense of personal destiny.
   *
   * @param input — Victory context.
   * @returns The drift result.
   * @see FR-1505
   */
  computeVictoryDrift(input: VictoryDriftInput): VictoryDriftResult {
    const pd = this.cfg.personalityDrift;
    const confidenceDelta = pd.victoryConfidenceBoost;
    const narcissismDelta = pd.victoryNarcissismBoost;

    const newConfidence = PersonalityDriftEngine.clamp(
      input.currentConfidence + confidenceDelta,
      0,
      100,
    );
    const newNarcissism = PersonalityDriftEngine.clamp(
      input.currentNarcissism + narcissismDelta,
      0,
      100,
    );

    const driftEvents: readonly DriftEvent[] = [
      {
        trigger: 'victory_confidence_drift',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'narcissism' as keyof LeaderPsychology,
        delta: confidenceDelta,
      },
      {
        trigger: 'victory_narcissism_drift',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'narcissism' as keyof LeaderPsychology,
        delta: narcissismDelta,
      },
    ];

    return {
      newConfidence,
      newNarcissism,
      confidenceDelta,
      narcissismDelta,
      driftEvents,
      reason: `Major victory — confidence +${confidenceDelta} (→ ${newConfidence}), ` +
        `narcissism +${narcissismDelta} (→ ${newNarcissism})`,
    };
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Near-Loss Drift
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute psychological drift from a near-loss recovery.
   *
   * Only triggers if the leader's stability once fell below the critical
   * threshold but has since recovered above it.  The experience of almost
   * losing everything hardens resolve and increases pragmatism.
   *
   * @param input — Near-loss context.
   * @returns The drift result.
   * @see FR-1505
   */
  computeNearLossDrift(input: NearLossDriftInput): NearLossDriftResult {
    const pd = this.cfg.personalityDrift;

    const droppedBelowThreshold =
      input.lowestStability < pd.nearLossStabilityThreshold;
    const recoveredAboveThreshold =
      input.currentStability >= pd.nearLossStabilityThreshold;

    if (!droppedBelowThreshold || !recoveredAboveThreshold) {
      return {
        triggered: false,
        newResolve: input.currentResolve,
        newPragmatism: input.currentPragmatism,
        resolveDelta: 0,
        pragmatismDelta: 0,
        driftEvents: [],
        reason: !droppedBelowThreshold
          ? `Lowest stability ${input.lowestStability} never fell below threshold ${pd.nearLossStabilityThreshold} — no near-loss drift`
          : `Current stability ${input.currentStability} still below threshold ${pd.nearLossStabilityThreshold} — recovery not yet complete`,
      };
    }

    const resolveDelta = pd.nearLossResolveBoost;
    const pragmatismDelta = pd.nearLossPragmatismBoost;

    const newResolve = PersonalityDriftEngine.clamp(
      input.currentResolve + resolveDelta,
      0,
      100,
    );
    const newPragmatism = PersonalityDriftEngine.clamp(
      input.currentPragmatism + pragmatismDelta,
      0,
      100,
    );

    const driftEvents: readonly DriftEvent[] = [
      {
        trigger: 'near_loss_resolve_drift',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'vengefulIndex' as keyof LeaderPsychology,
        delta: resolveDelta,
      },
      {
        trigger: 'near_loss_pragmatism_drift',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'pragmatism' as keyof LeaderPsychology,
        delta: pragmatismDelta,
      },
    ];

    return {
      triggered: true,
      newResolve,
      newPragmatism,
      resolveDelta,
      pragmatismDelta,
      driftEvents,
      reason: `Near-loss recovery (stability ${input.lowestStability} → ${input.currentStability}) — ` +
        `resolve +${resolveDelta} (→ ${newResolve}), pragmatism +${pragmatismDelta} (→ ${newPragmatism})`,
    };
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Stress Inoculation
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Compute stress inoculation status.
   *
   * After enough cumulative turns above the stress threshold a leader becomes
   * "stress-inoculated" — they gain a permanent reduction to incoming stress
   * gains and a pragmatism boost, at the cost of reduced ideological conviction.
   *
   * If the leader is already inoculated the method returns the ongoing modifier
   * without generating new drift events.
   *
   * @param input — Inoculation context.
   * @returns The inoculation result.
   * @see FR-1507
   */
  computeStressInoculation(input: InoculationInput): InoculationResult {
    const si = this.cfg.stressInoculation;

    // Already inoculated — return ongoing benefits, no new events.
    if (input.alreadyInoculated) {
      return {
        inoculated: true,
        newlyInoculated: false,
        stressGainModifier: si.stressGainReduction,
        pragmatismDelta: 0,
        ideologyDelta: 0,
        driftEvents: [],
        reason: 'Leader is already stress-inoculated — ongoing stress reduction active',
      };
    }

    // Not yet eligible.
    if (input.turnsAboveStressThreshold < si.turnsRequired) {
      return {
        inoculated: false,
        newlyInoculated: false,
        stressGainModifier: 0,
        pragmatismDelta: 0,
        ideologyDelta: 0,
        driftEvents: [],
        reason: `${input.turnsAboveStressThreshold}/${si.turnsRequired} turns above stress threshold ${si.stressThreshold} — inoculation not yet triggered`,
      };
    }

    // Newly inoculated this evaluation.
    const driftEvents: readonly DriftEvent[] = [
      {
        trigger: 'stress_inoculation_pragmatism',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'pragmatism' as keyof LeaderPsychology,
        delta: si.pragmatismBoost,
      },
      {
        trigger: 'stress_inoculation_ideology',
        eventId: null,
        turn: input.currentTurn,
        dimension: 'patience' as keyof LeaderPsychology,
        delta: si.ideologyReduction,
      },
    ];

    return {
      inoculated: true,
      newlyInoculated: true,
      stressGainModifier: si.stressGainReduction,
      pragmatismDelta: si.pragmatismBoost,
      ideologyDelta: si.ideologyReduction,
      driftEvents,
      reason: `Stress inoculation triggered after ${input.turnsAboveStressThreshold} turns — ` +
        `stress gain modifier ${si.stressGainReduction}, pragmatism +${si.pragmatismBoost}, ideology ${si.ideologyReduction}`,
    };
  }

  // ───────────────────────────────────────────────────────────────────────────
  // Private helpers
  // ───────────────────────────────────────────────────────────────────────────

  /**
   * Clamp a numeric value to the `[min, max]` range.
   */
  private static clamp(value: number, min: number, max: number): number {
    return Math.max(min, Math.min(max, value));
  }
}
