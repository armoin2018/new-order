/**
 * Result of trauma-response computation.
 *
 * @see FR-1506
 */
export interface TraumaResult {
  /** The trauma type that was processed. */
  readonly traumaType: TraumaType;
  /** Emotional dimension effects (positive = increase). */
  readonly emotionalEffects: {
    readonly fear?: number;
    readonly paranoia?: number;
    readonly stress?: number;
    readonly confidence?: number;
    readonly anger?: number;
    readonly resolve?: number;
  };
  /** Power-base adjustments. */
  readonly powerBaseEffects: {
    readonly securityServices?: number;
    readonly public?: number;
  };
  /** Fatigue accumulation multiplier (1 = normal). */
  readonly fatigueMultiplier: number;
  /** How many turns the fatigue multiplier persists. */
  readonly fatigueMultiplierDuration: number;
  /** Override to the leader's stress response, or `null` if unchanged. */
  readonly stressResponseOverride: 'escalate' | 'retreat' | null;
  /** Drift events generated by this trauma. */
  readonly driftEvents: readonly DriftEvent[];
  /** Human-readable explanation. */
  readonly reason: string;
}

