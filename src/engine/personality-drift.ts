/**
 * New Order: Personality Drift Engine
 *
 * Implements the personality-drift subsystem defined by FR-1505, FR-1506, and
 * FR-1507.  Leaders are not static — accumulated experience reshapes their
 * psychological profiles over time.  Prolonged stress shifts risk tolerance,
 * catastrophic events leave lasting trauma, betrayals fuel paranoia, and
 * victories inflate confidence.  After enough turns under extreme pressure a
 * leader can become *stress-inoculated*, gaining resilience at the cost of
 * ideological flexibility.
 *
 * All public methods are **pure functions**: they accept immutable inputs and
 * return new objects without mutating anything.  Config-driven thresholds and
 * multipliers are pulled from `GAME_CONFIG.psychology` so that balance tuning
 * requires no code changes (NFR-204).
 *
 * @module personality-drift
 * @see FR-1505 — Personality drift from accumulated experience
 * @see FR-1506 — Trauma response from catastrophic events
 * @see FR-1507 — Stress inoculation after prolonged high-stress exposure
 * @see NFR-204 — Config-driven formula tuning
 */

import type { LeaderId, TurnNumber, DriftEvent, LeaderPsychology } from '@/data/types';
import { TraumaType } from '@/data/types';
import { GAME_CONFIG } from './config';

// ─────────────────────────────────────────────────────────────────────────────
// Derived config type
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Shape of `GAME_CONFIG.psychology` extracted at the type level so that
 * consuming code can accept an arbitrary psychology configuration without
 * importing the full game config object.
 *
 * @see NFR-204
 */
export type PersonalityDriftConfig = typeof GAME_CONFIG.psychology;

// ─────────────────────────────────────────────────────────────────────────────
// Domain interfaces — Stress Drift
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Input for computing personality drift from prolonged high stress.
 *
 * @see FR-1505
 */
export interface StressDriftInput {
  /** The leader being evaluated. */
  readonly leaderId: LeaderId;
  /** How many consecutive turns the leader has been above the stress threshold. */
  readonly consecutiveHighStressTurns: number;
  /** The leader's current stress level (0–100). */
  readonly currentStress: number;
  /** The leader's innate stress response style. */
  readonly stressResponse: 'escalate' | 'consolidate' | 'retreat' | 'lash_out';
  /** The leader's current risk-tolerance value (0–100). */
  readonly currentRiskTolerance: number;
  /** Current game turn. */
  readonly currentTurn: TurnNumber;
}

/**
 * Result of stress-drift computation.
 *
 * @see FR-1505
 */
export interface StressDriftResult {
  /** Whether drift was triggered this evaluation. */
  readonly drifted: boolean;
  /** New risk-tolerance value after applying drift (clamped 0–100). */
  readonly newRiskTolerance: number;
  /** Signed delta applied to risk tolerance. */
  readonly delta: number;
  /** The drift event produced, or `null` if no drift occurred. */
  readonly driftEvent: DriftEvent | null;
  /** Human-readable explanation. */
  readonly reason: string;
}

